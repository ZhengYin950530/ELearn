package com.entity;

public class Student {
	
	private String student_id;
	private String student_name;
	private int student_sex;
	private int student_age;
	private int student_tel;
	private String student_address;
	private String student_email;
	
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public int getStudent_sex() {
		return student_sex;
	}
	public void setStudent_sex(int student_sex) {
		this.student_sex = student_sex;
	}
	public int getStudent_age() {
		return student_age;
	}
	public void setStudent_age(int student_age) {
		this.student_age = student_age;
	}
	public int getStudent_tel() {
		return student_tel;
	}
	public void setStudent_tel(int student_tel) {
		this.student_tel = student_tel;
	}
	public String getStudent_address() {
		return student_address;
	}
	public void setStudent_address(String student_address) {
		this.student_address = student_address;
	}
	public String getStudent_email() {
		return student_email;
	}
	public void setStudent_email(String student_email) {
		this.student_email = student_email;
	}

	
	
	
}
